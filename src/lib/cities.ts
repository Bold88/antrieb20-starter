export function slugifyCity(name: string): string {
  return name
    .toLowerCase()
    .normalize("NFD")                 // zerlegt Umlaute in Basis + Combining Marks
    .replace(/[\u0300-\u036f]/g, "")  // entfernt Combining Marks (Diakritika)
    .replace(/ß/g, "ss")
    .replace(/&/g, "und")             // optional: & → und
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)/g, "");
}

export function findCityBySlug(slug: string): string | null {
  for (const c of CITIES) {
    if (slugifyCity(c) === slug) return c;
  }
  return null;
}